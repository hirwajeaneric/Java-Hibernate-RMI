    private void courseRegistrationTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_courseRegistrationTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) courseRegistrationTable.getModel();
        int row = courseRegistrationTable.getSelectedRow();
        
        CourseIdField.setText(model.getValueAt(row, 0).toString());
        courseNameField.setText(model.getValueAt(row, 1).toString());
        creditsField.setText(model.getValueAt(row, 2).toString());
        semesterField.setSelectedItem(model.getValueAt(row, 3).toString());
        
    }//GEN-LAST:event_courseRegistrationTableMouseClicked


