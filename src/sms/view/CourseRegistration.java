    private void courseRegistrationSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseRegistrationSaveButtonActionPerformed
        try {
            // TODO add your handling code here:
            Courses cr = new Courses();
            
            cr.setCourseId(CourseIdField.getText());
            cr.setCourseName(courseNameField.getText());
            cr.setCredits(creditsField.getText());
            cr.setSemester(semesterField.getSelectedItem().toString());
            
            courseInterface.save(cr);
            
            updateTableCourseRegistration();
            resetFields();
            studentregistration.updateCourseCombobox();
            
            JOptionPane.showMessageDialog(this, "Course Successfully saved!!");
        } catch (RemoteException ex) {
            Logger.getLogger(CourseRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_courseRegistrationSaveButtonActionPerformed

